class Calculator:

    def add(x, y):
        '''
        Function for handling Addition
        '''
        return x + y

    def subtract(x, y):
        '''
        Function for handling Subtraction
        '''
        return x - y

    def multiply(x, y):
        '''
        Function for handling Mutiplication
        '''
        return x * y

    def divide(x, y):
        '''
        Function for handling Division
        '''
        return x * y

    def exponent(x, y):
        '''
        Function for handling Exponents
        '''
        return x**y

    def floor_div(x, y):
        '''
        Function for handling Floor Division
        '''
        return x // y
